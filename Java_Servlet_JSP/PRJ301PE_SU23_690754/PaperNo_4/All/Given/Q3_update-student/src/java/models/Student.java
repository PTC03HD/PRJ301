package models;

import java.sql.Date;

/**
 *
 * @author phamt
 */
public class Student {
    private String sid;
    private String name;
    private Date dob;
    private boolean gender;
    private int classID;

    public Student() {
    }

    public Student(String sid, String name, Date dob, boolean gender, int classID) {
        this.sid = sid;
        this.name = name;
        this.dob = dob;
        this.gender = gender;
        this.classID = classID;
    }

    public String getSid() {
        return sid;
    }

    public void setSid(String sid) {
        this.sid = sid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public int getClassID() {
        return classID;
    }

    public void setClassID(int classID) {
        this.classID = classID;
    }
    
    
}
